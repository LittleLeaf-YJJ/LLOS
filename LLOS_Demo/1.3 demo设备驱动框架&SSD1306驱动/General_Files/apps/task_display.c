void TaskDisplay_Init(void)
{
    taskDisplayId = LLOS_Register_Events(Task0_Events);
    if(taskDisplayId == LL_ERR_INVALID)
    {
    	LL_LOG_E("%s ", "init failed!\r\n", __FUNCTION__);
		while(1);
    }

	devGPIOA = LLOS_Device_Find("GPIOA");
	if(devGPIOA == NULL)
	{
    	LL_LOG_E("%s ", "GPIOA Not Found!\r\n", __FUNCTION__);
		while(1);
	}
	devSPI1 = LLOS_Device_Find("SPI1");
	if(devSPI1 == NULL)
	{
    	LL_LOG_E("%s ", "SPI1 Not Found!\r\n", __FUNCTION__);
		while(1);
	}
	devI2C2 = LLOS_Device_Find("I2C2");
	if(devI2C2 == NULL)
	{
    	LL_LOG_E("%s ", "I2C2 Not Found!\r\n", __FUNCTION__);
		while(1);
	}
	
	/* 屏幕初始化 */
	struct ll_SSD1306_conf_t SSD1306_conf[2] = {0};
	
	SSD1306_conf[0].devSPI_I2C = devSPI1;
	SSD1306_conf[0].devDC = devGPIOA;
	SSD1306_conf[0].devCS = devGPIOA;
	SSD1306_conf[0].pinDC = LL_BV(8);
	SSD1306_conf[0].pinCSorAddr = LL_BV(15);
	SSD1306_conf[0].isDMA = true;
	SSD1306_conf[0].screenConf.type = ll_SSD1306_screenType_128x64;
	SSD1306_conf[0].screenConf.xOffset = ll_SSD1306_screen_xOffset_0_96;
	SSD1306_conf[0].screenConf.isMirrot = false;
	SSD1306_conf[0].screenConf.isInvert = false;
	SSD1306_conf[0].screenConf.isInvertPhase = false;
	SSD1306_conf[0].screenConf.brightness = 255;

	SSD1306_conf[1].devSPI_I2C = devI2C2;
	SSD1306_conf[1].devDC = NULL;
	SSD1306_conf[1].devCS = NULL;
	SSD1306_conf[1].pinCSorAddr = 0x3C;
	SSD1306_conf[1].isDMA = true;
	SSD1306_conf[1].screenConf.type = ll_SSD1306_screenType_128x32;
	SSD1306_conf[1].screenConf.xOffset = ll_SSD1306_screen_xOffset_0_96;
	SSD1306_conf[1].screenConf.isMirrot = false;
	SSD1306_conf[1].screenConf.isInvert = false;
	SSD1306_conf[1].screenConf.isInvertPhase = false;
	SSD1306_conf[1].screenConf.brightness = 255;
	
	LLOS_SSD1306_HAL_Init(SSD1306_conf, 2);
	LLOS_Graphic_Register_DrawDot(LLOS_SSD1306_DrawDot);
	
	for(uint8_t i = 0; i < 2; i++)
	{
		LLOS_SSD1306_HAL_Select(i);
		
		LLOS_SSD1306_Fill(0xAA);
		
		LLOS_DelayMs(1000);
		LLOS_SSD1306_Fill(0x00);
		LLOS_SSD1306_ShowString(0, 0, "您好，小叶子。数学℃\r\nHello LLOS");
		
		LLOS_DelayMs(1000);
		LLOS_SSD1306_Fill(0x00);
		LLOS_SSD1306_ShowNumFormat(0, 0, 32767, "AAA:%08X", ll_SSD1306_sizeFont_8x16);
		LLOS_SSD1306_ShowNumFormat(0, 2, 32767, "%08X BBB", ll_SSD1306_sizeFont_6x8);
		LLOS_SSD1306_ShowNumFormat(0, 4, -32767, "A %d B", ll_SSD1306_sizeFont_8x16);
		LLOS_SSD1306_ShowNumFormat(0, 6, -3.14159, "Float:%.3f", ll_SSD1306_sizeFont_8x16);
		
		LLOS_DelayMs(1000);
		LLOS_SSD1306_Fill(0x00);
		LLOS_Graphic_DrawRectangle(0, 0, 16, 16, ll_disable, 0);
		LLOS_Graphic_DrawRectangle(30, 0, 16, 16, ll_enable, 0);
		LLOS_Graphic_DrawRoundedRectangle(60, 0, 30, 16, 5, ll_disable, 0);
		LLOS_Graphic_DrawRoundedRectangle(96, 0, 30, 16, 5, ll_enable, 0);
		LLOS_Graphic_DrawCircle(8, 30, 8, ll_disable, 0);
		LLOS_Graphic_DrawCircle(38, 30, 8, ll_enable, 0);
		LLOS_Graphic_DrawTriangle(5, 60, 15, 60, 10, 50, ll_disable, 0);
		LLOS_Graphic_DrawTriangle(35, 60, 45, 60, 40, 50, ll_enable, 0);
		
		LLOS_DelayMs(1000);
		LLOS_SSD1306_Fill(0x00);
	}
	
	LLOS_Start_Event(taskDisplayId, TASK_DISPLAY_EVENT1, LLOS_Ms_To_Tick(0));
}

